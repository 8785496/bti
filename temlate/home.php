            $(document).ready(function () {
                $('#contactForm').on('submit', function (e) {
                    e.preventDefault();
                    e.stopPropagation();

                    // get values from FORM                     
                    var name = $("#name").val();
                    var email = $("#email").val();
                    var message = $("#message").val();
                    var goodToGo = false;
                    var messgaeError = 'Request can not be send';
                    var pattern = new RegExp(/^(('[\w-\s]+')|([\w-]+(?:\.[\w-]+)*)|('[\w-\s]+')([\w-]+(?:\.[\w-]+)*))(@((?:[\w-]+\.)*\w[\w-]{0,66})\.([a-z]{2,6}(?:\.[a-z]{2})?)$)|(@\[?((25[0-5]\.|2[0-4][0-9]\.|1[0-9]{2}\.|[0-9]{1,2}\.))((25[0-5]|2[0-4][0-9]|1[0-9]{2}|[0-9]{1,2})\.){2}(25[0-5]|2[0-4][0-9]|1[0-9]{2}|[0-9]{1,2})\]?$)/i);

                    if (name && name.length > 0 && $.trim(name) != '' && message && message.length > 0 && $.trim(message) != '' && email && email.length > 0 && $.trim(email) != '') {
                        if (pattern.test(email)) {
                            goodToGo = true;
                        } else {
                            messgaeError = 'Пожалуйста, проверьте Ваш e-mail адрес';
                            goodToGo = false;
                        }
                    } else {
                        messgaeError = 'Чтобы продолжить, заполните все поля!';
                        goodToGo = false;
                    }

                    if (goodToGo) {
                        $.ajax({
                            data: $('#contactForm').serialize(),
                            beforeSend: function () {
                                $('#success').html('<div class="col-md-12 text-center"><img src="images/spinner1.gif" alt="spinner" /></div>');
